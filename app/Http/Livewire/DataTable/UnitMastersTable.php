    |--------------------------------------------------------------------------
    |  Add Column
    |--------------------------------------------------------------------------
    | Make Datasource fields available to be used as columns.
    | You can pass a closure to transform/modify the data.
    |
    | â— IMPORTANT: When using closures, you must escape any value coming from
    |    the database using the `e()` Laravel Helper function.
    |
    */
    public function addColumns(): PowerGridEloquent
    {
        return PowerGrid::eloquent()
            ->addColumn('id')
            ->addColumn('unit_name')

            /** Example of custom column using a closure **/
            ->addColumn('unit_name_lower', function (UnitMaster $model) {
                return strtolower(e($model->unit_name));
            })

            ->addColumn('short_name')
            ->addColumn('is_active', function (UnitMaster $unitMaster) {
                if ($unitMaster->is_active == 0) {
                    return '<label class="badge badge-pill bg-danger cursor-pointer">Inactive</label>';
                } else {
                    return '<label class="badge badge-pill bg-success cursor-pointer">Active</label>';
                }
            });
    }

